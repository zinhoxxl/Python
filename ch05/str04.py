print ("---------------------- Character -------------------")
print('1234'.isdigit())   # 문자열 내의 Character 들이 모두 숫자인가?
print('abcd'.isalpha())   # 문자열 내의 Character 들이 모두 영문자인가?
print('1abc234'.isalnum())    # 문자열 내의 Character 들이 모두 영문자 또는 숫자인가?
print('abc'.islower())    # 문자열 내의 Character 들이 모두 소문자인가?
print('ABC'.isupper())    # 문자열 내의 Character 들이 모두 대문자인가?
print('\t\r\n'.isspace())    # 문자열 내의 Character 들이 모두 공백 문자인가?
print('This Is A Title'.istitle())    # 문자열이 Title 형식 (각 단어의 첫글자가 대문자) 인가?
