# 연산자 축약 ( +=, -=, *=, /= )
a = 0
a += 1                  # a = a + 1
print('a=', a)          # 1 출력

a -= 5                  # a = a - 5
print('a=', a)          # -4 출력

a *= 2                  # a = a * 2
print('a=', a)          # -8 출력

a /= 4                  # a = a / 4
print('a=', a)          # -2.0 출력